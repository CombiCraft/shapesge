package fri.shapesge;

/**
 * This is an <b>enum</b> that contains the
 * possible font styles for use with {@link TextBlock#changeFont}.
 */
@SuppressWarnings("unused")
public enum FontStyle {
    /**
     * Plain font style.
     */
    PLAIN,
    /**
     * Bold font style.
     */
    BOLD,
    /**
     * Italic font style.
     */
    ITALIC,
    /**
     * Underlined font style.
     */
    UNDERLINE
}
