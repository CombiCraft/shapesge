package fri.shapesge;

/**
 * Toto je <b>enum</b>, ktorý obsahuje
 * štýly písma pre {@link BlokTextu#zmenFont}.
 */
@SuppressWarnings("unused")
public enum StylFontu {
    /**
     * Normálny štýl písma.
     */
    PLAIN,
    /**
     * Tučný štýl písma.
     */
    BOLD,
    /**
     * Šikmý štýl písma.
     */
    ITALIC,
    /**
     * Podčiarknutý štýl písma.
     */
    UNDERLINE
}
